package day02_escapeSequences;

public class EscapeSequances {

    public static void main(String[] args) {

        System.out.println("Hello students.\nToday we have Java class");

        System.out.println("------------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch1");

        System.out.println("---------------------------------");

        System.out.println('\\');

        System.out.println("----------------------------------");

        System.out.println("My favorite TV show is \"Nat Geo Wild\"");


    }



}

/*
1. \n: New Line
2. \t: paragraph space
3. \\: Back slash
4. \*: double quote
 */
